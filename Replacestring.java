package com.java;

public class Replacestring {

	
	public static void main(String[] args) {
		
		String s="Home is sweet home", ref=" ";
		char c;
		for (int i = 0; i < s.length(); i++) {
			c=s.charAt(i);
			ref=c+ref;
		}
		System.out.println(ref);
		
	}
		
		}                   



































